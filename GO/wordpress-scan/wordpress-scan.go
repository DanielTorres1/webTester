func checkRoutes(client *http.Client, url string, routes []Route, results chan<- Result) {
	for _, route := range routes {
		resp, err := client.Head(url + route.Path)
		result := Result{Name: route.Name, CVE: route.cve}
		if err != nil {
			result.Error = err
		} else {
			defer resp.Body.Close()
			result.StatusCode = resp.StatusCode
			if resp.StatusCode == 200 || resp.StatusCode == 403 {
				result.Result = "Probable Vulnerable"
				if resp.StatusCode == 200 {
					resp, _ := client.Get(url + route.Path + "/readme.txt")
					defer resp.Body.Close()
					body, err := ioutil.ReadAll(resp.Body)
					if err != nil {
						fmt.Println("Error reading response body:", err)
						return
					}

					re := regexp.MustCompile(`Stable tag:\s*(\S+)`)
					matches := re.FindStringSubmatch(string(body))
					if len(matches) > 1 {
						result.Version =  matches[1]
					} else {
						result.Version = ""
					}

				}
				
			} else {
				result.Result = "OK"
			}
		}
		results <- result
	}
}

func main() {
	url := flag.String("url", "", "URL to check")
	flag.Parse()

	if *url == "" {
		fmt.Println("Usage: go run script.go -url=<url>")
		os.Exit(1)
	}

	tr := &http.Transport{
		TLSClientConfig: &tls.Config{InsecureSkipVerify: true},
	}
	client := &http.Client{Transport: tr, Timeout: time.Duration(10 * time.Second)}

	// Verificacin de la ruta inexistente
	resp, err := client.Head(*url + "/wp-content/plugins/nonexits123/")
	if err != nil {
		fmt.Printf("Error al realizar la verificacin inicial: %v\n", err)
		os.Exit(1)
	}
	defer resp.Body.Close()

	if resp.StatusCode == 200 {
		fmt.Println("El servidor retorna 200 OK para rutas inexistentes. No se puede confiar en los resultados.")
		os.Exit(1)
	}

	results := make(chan Result)
	var wg sync.WaitGroup

	numGoroutines := 5
	routesPerGoroutine := (len(routes) + numGoroutines - 1) / numGoroutines

	for i := 0; i < numGoroutines; i++ {
		start := i * routesPerGoroutine
		end := start + routesPerGoroutine
		if end > len(routes) {
			end = len(routes)
		}

		wg.Add(1)
		go func(start, end int) {
			defer wg.Done()
			checkRoutes(client, *url, routes[start:end], results)
		}(start, end)
	}

	go func() {
		wg.Wait()
		close(results)
	}()

	fmt.Println("Nombre | version | Status Code | Resultado | CVE")
	for result := range results {
		if result.Error != nil {
			fmt.Printf("%s | Error | %v\n", result.Name, result.Error)
		} else {
			fmt.Printf("%s %s | %d | %s | %s\n", result.Name, result.Version, result.StatusCode, result.Result, result.CVE)
		}
	}
}
